package satish;

public class satishMath {
	
	//to return the result of addition of two numbers
	public int sum(int x,int y)
	{
		return x+y;
	}
	
	//to return the result of subtraction of two numbers
	public int subtract(int x,int y)
	{
		return x-y;
	}
	
	public int division(int x,int y)
	{
		return x/y;
	}

}
